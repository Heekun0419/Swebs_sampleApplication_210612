package com.example.swebs_sampleapplication_210612.ViewModel.Model;

import androidx.annotation.Nullable;

public class UserInfo {
    @Nullable
    private String userSrl;
    @Nullable
    private String userType;
    @Nullable
    private String token;
    @Nullable
    private String nickName;
    @Nullable
    private String name;
    @Nullable
    private String birthday;
    @Nullable
    private String gender;
    @Nullable
    private String phoneNumber;
    @Nullable
    private String country;
    @Nullable
    private String region;
    @Nullable
    private String email;
    @Nullable
    private String referralCode;
    @Nullable
    private String point;

    @Nullable
    public String getUserSrl() {
        return userSrl;
    }

    public void setUserSrl(@Nullable String userSrl) {
        this.userSrl = userSrl;
    }

    @Nullable
    public String getUserType() {
        return userType;
    }

    public void setUserType(@Nullable String userType) {
        this.userType = userType;
    }

    @Nullable
    public String getToken() {
        return token;
    }

    public void setToken(@Nullable String token) {
        this.token = token;
    }

    @Nullable
    public String getNickName() {
        return nickName;
    }

    public void setNickName(@Nullable String nickName) {
        this.nickName = nickName;
    }

    @Nullable
    public String getName() {
        return name;
    }

    public void setName(@Nullable String name) {
        this.name = name;
    }

    @Nullable
    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(@Nullable String birthday) {
        this.birthday = birthday;
    }

    @Nullable
    public String getGender() {
        return gender;
    }

    public void setGender(@Nullable String gender) {
        this.gender = gender;
    }

    @Nullable
    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(@Nullable String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    @Nullable
    public String getCountry() {
        return country;
    }

    public void setCountry(@Nullable String country) {
        this.country = country;
    }

    @Nullable
    public String getRegion() {
        return region;
    }

    public void setRegion(@Nullable String region) {
        this.region = region;
    }

    @Nullable
    public String getEmail() {
        return email;
    }

    public void setEmail(@Nullable String email) {
        this.email = email;
    }

    @Nullable
    public String getReferralCode() {
        return referralCode;
    }

    public void setReferralCode(@Nullable String referralCode) {
        this.referralCode = referralCode;
    }

    @Nullable
    public String getPoint() {
        return point;
    }

    public void setPoint(@Nullable String point) {
        this.point = point;
    }
}
